function GetLoadingMap(mapName, modeName)
  local map_name = string.lower(mapName)
  if not modeName or not gametype_loading_map[modeName] then
    print("Error : not valid gametype")
    return nil
  end
  return Gui.Image(string.format("/ui/MapsAndBG/Loading/%s", gametype_loading_map[modeName]))
end

function GetTeamLogo(logoName)
  local index = tonumber(logoName)
  if index and 0 < index and index < 6 then
    return TeamLogos[index]
  else
    return TeamLogos[1]
  end
end

